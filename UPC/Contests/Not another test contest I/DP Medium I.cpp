const int N = 1000+5;

int q;
int n, m;
bool vis[N][N];
int diag[N][N];
double memo[N][N];
int dx[] = {1,0,1};
int dy[] = {0,1,1};
double cost[] = {100, 100, 100 * sqrt(2)};

bool validPos(int i, int j){
	return i >= 0 and j >= 0 and i <= n and j <= m;
}

double DP(int x, int y){
	if(x == n and y == m) return 0.0;
	if(vis[x][y]) return memo[x][y];
	double ans = 1e19;
	for(int i=0; i < 2 + diag[x][y]; i++){
		int vx = x + dx[i];
		int vy = y + dy[i];
		if(validPos(vx,vy)){
			ans = min(ans, cost[i] + DP(vx,vy));
		}
	}
	vis[x][y] = true;
	return memo[x][y] = ans;
}

int main(){
	ri2(n,m);
	ri(q);
	for(int i=0; i<q; i++){
		int x, y;
		ri2(x,y);
		x--; y--;
		diag[x][y] = 1;
	}
	double ans = DP(0,0);
	cout << round(ans) << endl;
	return 0;
}
